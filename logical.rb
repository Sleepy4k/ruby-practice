# I think for logical operator in ruby it's same with other programming language.
#
# So we can use && for and operator
# || for or operator
# ! for not operator
# == for equal operator
# != for not equal operator
# > for greater than operator
# < for less than operator
# >= for greater than or equal operator
# <= for less than or equal operator

puts 1 == 1 && 2 == 2
puts 1 == 1 && 2 == 3

puts 1 == 1 || 2 == 2
puts 1 == 1 || 2 == 3

puts !(1 == 1)
puts !(1 == 2)

puts 1 != 2
puts 1 != 1

puts 1 > 2
puts 1 < 2

puts 1 >= 1
puts 1 <= 1
